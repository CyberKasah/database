USE [Учебная_Колесник]
/* 1. Вывести ФИО, специализацию и дату рождения всех академиков */
SELECT [Фамилия] + ' ' + [Имя] + ' ' + [Отчество] AS [ФИО], [Специализация], [Дата_рождения] FROM [Академики];
/* 2. Создать вычисляемое поле «О присвоении звания» */
SELECT [Фамилия] + ' ' + [Имя] + ' ' + [Отчество] AS [ФИО], 'Получил звание в ' + CAST([Год_присвоения_звания] AS VARCHAR) AS [О присвоении звания] FROM [Академики];
/* 3. Вывести ФИО академиков и поле «Через 5 лет после присвоения звания» */
SELECT [Фамилия] + ' ' + [Имя] + ' ' + [Отчество] AS [ФИО], [Год_присвоения_звания] + 5 AS [Через 5 лет после присвоения звания] FROM [Академики];
/* 4. Вывести список годов присвоения званий, убрав дубликаты */
SELECT DISTINCT [Год_присвоения_звания] FROM [Академики];
/* 5. Вывести список академиков по убыванию даты рождения */
SELECT [Фамилия] + ' ' + [Имя] + ' ' + [Отчество] AS [ФИО], [Дата_рождения] FROM [Академики] ORDER BY [Дата_рождения] DESC;
/* 6. Сортировка по специализации (убыв.), году (убыв.) и ФИО (возр.) */
SELECT [Фамилия] + ' ' + [Имя] + ' ' + [Отчество] AS [ФИО], [Специализация], [Год_присвоения_звания] FROM [Академики] ORDER BY [Специализация] DESC, [Год_присвоения_звания] DESC, [Фамилия], [Имя], [Отчество];
/* 7. Первая строка по убыванию ФИО */
SELECT TOP 1 [Фамилия] + ' ' + [Имя] + ' ' + [Отчество] AS [ФИО] FROM [Академики] ORDER BY [Фамилия] DESC, [Имя] DESC, [Отчество] DESC;
/* 8. Фамилия академика, получившего звание первым */
SELECT TOP 1 [Фамилия] FROM [Академики] ORDER BY [Год_присвоения_звания];
/* 9. Первые 10% строк по ФИО */
SELECT TOP 10 PERCENT [Фамилия] + ' ' + [Имя] + ' ' + [Отчество] AS [ФИО] FROM [Академики] ORDER BY [Фамилия], [Имя], [Отчество];
/* 10. Первые пять по году присвоения звания (с учётом повторов) */
SELECT TOP 5 WITH TIES [Фамилия] + ' ' + [Имя] + ' ' + [Отчество] AS [ФИО], [Год_присвоения_звания] FROM [Академики] ORDER BY [Год_присвоения_звания];
/* 11. С десятого по возрасту (дата рождения) */
SELECT [Фамилия] + ' ' + [Имя] + ' ' + [Отчество] AS [ФИО], [Дата_рождения] FROM [Академики] ORDER BY [Дата_рождения] OFFSET 9 ROWS;
/* 12. Девятая и десятая строки по алфавиту ФИО */
SELECT [Фамилия] + ' ' + [Имя] + ' ' + [Отчество] AS [ФИО] FROM [Академики] ORDER BY [Фамилия], [Имя], [Отчество] OFFSET 8 ROWS FETCH NEXT 2 ROWS ONLY;
